package com.example;

public class No {
	private String nome;
	private int distancia;
	
	public No(String nome, int distancia) {
		this.nome = nome;
		this.distancia = distancia;
	}
	
	public String getNome() {
		return nome;
	}
	
	public int getDistancia() {
		return distancia;
	}
}
